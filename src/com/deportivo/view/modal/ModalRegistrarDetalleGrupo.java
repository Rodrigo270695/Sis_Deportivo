    private void cargarEquipos() {
    
        cbxEquipo.removeAllItems();
        
        List<Equipo> lista = equipoC.listar();

        for (Equipo equipo : lista) {
            cbxEquipo.addItem(equipo.getNombreCorto());
        }
    }

    void grabar() {

        Equipo equipo = (Equipo) equipoC.obtenerdato(cbxEquipo.getSelectedItem().toString());
        
        try {
            detalleGC.registrarDetalle(idGrupo, equipo.getEquipoId());
            FrmGestionarDetalleGrupo.listar("");
            AlertaBien bien = new AlertaBien("MENSAJE", "Se agreg√≥ correctamente el equipo al grupo");
            dispose();

        } catch (Exception e) {
            e.printStackTrace(System.err);
            AlertaError error = new AlertaError("Error", e.getMessage());
        }

    }

