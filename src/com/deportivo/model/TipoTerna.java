package com.deportivo.model;

public class TipoTerna {

    private int tipoTernaId;
    private String nombre;
    private String sigla;

    public int getTipoTernaId() {
        return tipoTernaId;
    }

    public void setTipoTernaId(int tipoTernaId) {
        this.tipoTernaId = tipoTernaId;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getSigla() {
        return sigla;
    }

    public void setSigla(String sigla) {
        this.sigla = sigla;
    }

  

}
